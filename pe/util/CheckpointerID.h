#ifndef _PE_UTIL_CHECKPOINTERID_H_
#define _PE_UTIL_CHECKPOINTERID_H_

//*************************************************************************************************
// Includes
//*************************************************************************************************

#include <boost/shared_ptr.hpp>


namespace pe {

//=================================================================================================
//
//  TYPE DEFINITIONS
//
//=================================================================================================

class Checkpointer;


//*************************************************************************************************
/*!\brief Handle for a Checkpointer.
 */
typedef boost::shared_ptr<Checkpointer>       CheckpointerID;
//*************************************************************************************************


//*************************************************************************************************
/*!\brief Handle for a constant Checkpointer.
 */
typedef boost::shared_ptr<const Checkpointer> ConstCheckpointerID;
//*************************************************************************************************

} // namespace pe

#endif // _PE_UTIL_CHECKPOINTERID_H_
